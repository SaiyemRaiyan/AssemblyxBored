.MODEL SMALL
.STACK 100H

.DATA
M DB 'I LOVE BANGLADESH $'

.CODE

MOV AX, @DATA
MOV DS, AX

MOV AH, 9
LEA DX, M ; LOAD EFFECTIVE REGISTER
INT 21H  

MOV AH, 1
INT 21H
MOV BL, AL ; INPUT OK!

MOV AH, 2
MOV DL, BL
INT 21H   ; EXECUTE

EXIT:
 MOV AH, 4CH
 INT 21H
 
END 


  ; 1 -> SINGLE KEY INPUT
  ; 2 -> SINGLE CHARACTER OUTPUT
  ; 9 -> CHARACTER STRING OUTPUT