INCLUDE 'EMU8086.INC'      

.MODEL SMALL
.STACK 100H

.DATA


.CODE
   MOV AH, 1       ; INPUT   
   INT 21H 
   MOV AH,0
   MOV BL,2
   DIV BL
    
   
   CMP AH,0
   JE PRINT_E

   PRINT_O:
   MOV AH, 2
   MOV DL, 'O'   
   INT 21H
   
   JMP EXIT
   
   PRINT_E:
   MOV AH, 2
   MOV DL, 'E'
   INT 21H
   
    
EXIT:
    MOV AH, 4CH
    INT 21H


